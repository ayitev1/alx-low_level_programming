#include "main.h"

/**
 * _isalpha - Entry point
 * @c: a parameter to check for lowercase
 * this function is to check for lowercase character
 * Return: 0 Always - Success
 */
int _isalpha(int c);
{
	char ch;
	int i;

	for (ch = 'a' || ch = 'A'; ch <= 'z' || ch <= 'Z'; ch++)
	{
		if (ch == c)
			i = 1;
	}

	return (i);
}
